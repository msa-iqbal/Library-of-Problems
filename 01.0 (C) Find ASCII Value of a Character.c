/// (C) Find a Character of ASCII Value
#include <stdio.h>
#include <stdlib.h>

int main()
{
    int a;
    printf("Enter any ASCII value: ");
    scanf("%d", &a);

    printf("The ASCII Character is: %c \n", a);
    return 0;
}


/* ===== Output / Result:
Input:
Enter any ASCII value: 65

Output:
The ASCII Character is: A
*/
