/// Convert from hexa-decimal to decimal
#include <stdio.h>

int main()
{
    int num;
    printf("Enter the hexa-decimal number: ");
    scanf("%x", &num);

    printf("Decimal number is: %d", num);
    return 0;
}

/* ===== Output / Result:
Input:
Enter the hexa-decimal number: f

Output:
Decimal number is: 15
*/








