/// Convert from decimal to octal
#include <stdio.h>

int main()
{
    int num;
    printf("Enter the decimal number: ");
    scanf("%d", &num);

    printf("Octal number is: %o", num);
    return 0;
}

/* ===== Output / Result:
Input:
Enter the decimal number: 25

Output:
Octal number is: 31
*/



