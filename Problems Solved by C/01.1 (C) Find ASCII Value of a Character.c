/// Find ASCII Value of a Character
#include <stdio.h>
#include <stdlib.h>

int main()
{
    char a;

    printf("Enter any ASCII Character: ");
    scanf("%c", &a);

    printf("The ASCII Value is: %d \n", a);
    return 0;
}

/* ===== Output / Result:
Input:
Enter any ASCII Character: A

Output:
The ASCII Value is: 65
*/


