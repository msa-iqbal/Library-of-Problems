/// Convert from Octal to Decimal
#include <stdio.h>

int main()
{
    int num;
    printf("Enter the octal number: ");
    scanf("%o", &num);

    printf("Decimal number is: %d", num);
    return 0;
}

/* ===== Output / Result:
Input:
Enter the octal number: 31

Output:
Decimal number is: 25
*/


