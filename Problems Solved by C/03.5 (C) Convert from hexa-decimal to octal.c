/// Convert from hexa-decimal to octal
#include <stdio.h>

int main()
{
    int num;
    printf("Enter the hexa-decimal number: ");
    scanf("%x", &num);

    printf("Octal number is: %o", num);
    return 0;
}

/* ===== Output / Result:
Input:
Enter the hexa-decimal number: f

Output:
Octal number is: 17
*/




