/// Convert from decimal to hexa-decimal
#include <stdio.h>

int main()
{
    int num;
    printf("Enter the decimal number: ");
    scanf("%d", &num);

    printf("Hexa-decimal number is: %x", num);
    return 0;
}

/* ===== Output / Result:
Input:
Enter the decimal number: 15

Output:
Hexa-decimal number is: f
*/






