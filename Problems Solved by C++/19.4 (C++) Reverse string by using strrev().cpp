/// Reverse String from given string by using strrev()

#include <iostream>
#include <cstring>
using namespace std;

int main()
{
    char name [] = "Michael Scofield";

    strrev(name); //Only Support One parameter
    cout<<" The reverse string is: "<<name<<endl;
    return 0;
}


